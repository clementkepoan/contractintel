const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => window.localStorage.getItem("dashboard-lang") || "en");

  useEffect(() => {
    window.localStorage.setItem("dashboard-lang", lang);
    document.documentElement.lang = lang;
  }, [lang]);

  const value = useMemo(() => {
    function t(path, fallback = "") {
      const segments = path.split(".");
      let cursor = translations[lang] || translations.en;
      for (const segment of segments) {
        cursor = cursor?.[segment];
      }
      if (typeof cursor === "string") return cursor;
      if (fallback) return fallback;
      let englishCursor = translations.en;
      for (const segment of segments) {
        englishCursor = englishCursor?.[segment];
      }
      return typeof englishCursor === "string" ? englishCursor : path;
    }
    return { lang, setLang, t };
  }, [lang]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used within I18nProvider");
  return value;
}
